// comparison between two variables

let num1 = 22;
let num2 = 28;

// greater than
let greaterThan = num1 > num2;
console.log("num1 is greater Than num2 = " + greaterThan);

// less than
let lessThan = num1 < num2;
console.log("num1 is less than num2 = " + lessThan);

// equal to
let equalTo = num1 == num2;
console.log("num1 is equal to num2 = " + equalTo);

// not equal to
let notEqualTo = num1 != num2;
console.log("num1 is not equal to num2 = " + notEqualTo);

// greater than or equal to
let greaterThanOrEqualTo = num1 >= num2;
console.log("num1 is greater than or equal to num2 = " + greaterThanOrEqualTo);

// less than or equal to
let lessThanOrEqualTo = num1 <= num2;
console.log("num1 is less than or equal to num2 = " + lessThanOrEqualTo);